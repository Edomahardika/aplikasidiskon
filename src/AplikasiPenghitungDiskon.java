    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        try {
        double hargaAsli = Double.parseDouble(txtHargaAsli.getText());
        String diskonStr = cmbDiskon.getSelectedItem().toString();

        // Pastikan pengguna sudah pilih diskon
        if (diskonStr.equals("Pilih Diskon")) {
            javax.swing.JOptionPane.showMessageDialog(this, "Silakan pilih persentase diskon terlebih dahulu.");
            return;
        }

        // Ubah "20%" jadi 0.20
        double diskonPersen = Double.parseDouble(diskonStr.replace("%", "")) / 100.0;

        // Kupon tambahan (opsional)
        String kodeKupon = txtKupon.getText().trim();
        double tambahanDiskon = 0.0;
        if (kodeKupon.equalsIgnoreCase("HEMAT10")) {
            tambahanDiskon = 0.10; // Tambahan 10%
        }

        double totalDiskon = diskonPersen + tambahanDiskon;
        if (totalDiskon > 1) totalDiskon = 1; // maksimum 100%

        double jumlahHemat = hargaAsli * totalDiskon;
        double hargaAkhir = hargaAsli - jumlahHemat;

        // Tampilkan hasil
        txtHargaAkhir.setText(String.format("%.2f", hargaAkhir));
        txtHemat.setText(String.format("%.2f", jumlahHemat));

        // Tambahkan ke riwayat
        txtRiwayat.append(
        String.format(
        "Nama Barang : %s%n" +
        "Harga Asli  : Rp %.2f%n" +
        "Diskon      : %.0f%% + Kupon %.0f%%%n" +
        "Harga Akhir : Rp %.2f%n" +
        "Hemat       : Rp %.2f%n" +
        "----------------------------------------------------------------\n",
        txtNamaBarang.getText(),
        hargaAsli,
        diskonPersen * 100,
        tambahanDiskon * 100,
        hargaAkhir,
        jumlahHemat
    )
);


    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Masukkan harga asli dengan benar (angka).");
    }
    }//GEN-LAST:event_btnHitungActionPerformed

